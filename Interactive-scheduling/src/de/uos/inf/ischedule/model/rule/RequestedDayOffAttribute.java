package de.uos.inf.ischedule.model.rule;

import de.uos.inf.ischedule.model.Solution;

/**
 * Attribute for day off requests.
 * 
 * @author David Meignan
 */
public class RequestedDayOffAttribute extends BooleanAttribute {

	/**
	 * Key of the attribute.
	 */
	private String key = "requested-day-off";
	
	/* (non-Javadoc)
	 * @see de.uos.inf.ischedule.model.rule.Attribute#getKey()
	 */
	@Override
	public String getKey() {
		return key;
	}

	/* (non-Javadoc)
	 * @see de.uos.inf.ischedule.model.rule.Attribute#getValue(int, int, de.uos.inf.ischedule.model.Solution, boolean, boolean)
	 */
	@Override
	public Boolean getValue(int dayIndex, int employeeIndex, Solution solution,
			boolean isPreferred, boolean isUnwanted) {
		return new Boolean(solution.employees.get(employeeIndex).hasDayOffRequest(
				solution.problem.getSchedulingPeriod().getDate(dayIndex)));
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		RequestedDayOffAttribute other = (RequestedDayOffAttribute) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}

}
